import cv2
import numpy as np
img=cv2.imread(r'C:\Users\mohit\Downloads\rose_flower.jpg')
kernel=np.array(([0,0,0],[0,3,0],[0,0,0]),np.uint8)
blurr=cv2.filter2D(img,-1,kernel)
cv2.imshow("img",img)
cv2.imshow("blurr",blurr)
avg=cv2.blur(img,(5,5))
cv2.imshow("avg",avg)
bilateral=cv2.bilateralFilter(img,5,75,75)
gaus_blur=cv2.GaussianBlur(img,(5,5),0)
median=cv2.medianBlur(img,5)
cv2.imshow("bil",bilateral)
cv2.imshow("gaus",gaus_blur)
cv2.imshow("median",median)
print(img.shape)
pts=np.array([[42,550],[132,550],[127,790],[47,790]])
pts=pts.reshape((-1,1,2))
img1=cv2.polylines(img,[pts],True,(0,0,0),10)
font=cv2.FONT_HERSHEY_COMPLEX
cv2.putText(img1,'Rose',(310,53),font,2,(0,0,0))
cv2.imshow("fpot",img1)

bgrl=np.array([98,0,120],np.uint8)
bgrh=np.array([215,255,255],np.uint8)
mask=cv2.inRange(img,bgrl,bgrh)


cv2.imshow("mask",mask)
cv2.imwrite('mask.jpg',mask)
cv2.imwrite('filter2D.jpg',blurr)
cv2.imwrite('avg.jpg',avg)
cv2.imwrite('bilateral.jpg',bilateral)
cv2.imwrite('gauss_blur.jpg',gaus_blur)
cv2.imwrite('median.jpg',median)
cv2.imwrite('fpot.jpg',img1)
cv2.waitKey(0)
cv2.destroyAllWindows()